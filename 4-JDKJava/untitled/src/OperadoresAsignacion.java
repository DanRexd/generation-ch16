public class OperadoresAsignacion {
    public static void main (String [] args){

        int i = 5;
        int j = i + 4;
        System.out.println("i es igual a: " + i );
        System.out.println("j es igual a: " + j );

        i+=2;
        System.out.println("La nueva i es igual a: " + i);

        j-=4;
        System.out.println("La nueva j es igual a : " + j);

        i*=5;
        System.out.println("La nueva i es igual a : " + i);

    }
}
